import type { Asset } from "contentful";
import Head from "next/head";
import { useState, useEffect } from "react";
import { getAssets } from "../../contentful";
import AudioPlayer from "../components/AudioPlayer";
import Navbar from "../components/Navbar";
import styled from "styled-components";

export async function getStaticProps() {
  const assets = await getAssets();
  const items = assets.items;
  return {
    props: {
      items,
    },
  };
}

interface Props {
  items: Asset[];
  isDarkMode: boolean;
  setDarkMode: (isDarkMode: boolean) => void;
}

export default function Soundboard({ items, isDarkMode, setDarkMode }: Props) {
  const [volume, setVolume] = useState(0.25);
  const [navbarCollapsed, setNavbarCollapsed] = useState(true);
  const setNewVolume = (volume: number) => {
    setVolume(volume);
    localStorage.setItem("volume", volume.toString());
  }

  useEffect(() => {
    const localVolume = localStorage.getItem("volume");
    if (localVolume) setVolume(Number(localVolume))
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Navbar
        isDarkMode={isDarkMode}
        setDarkMode={setDarkMode}
        volume={volume}
        setVolume={setNewVolume}
        collapsed={navbarCollapsed}
        setCollapsed={setNavbarCollapsed}
      />
      <Main navbarCollapse={navbarCollapsed}>
        <Container>
          {items.map((item) => (
            <li key={item.sys.id}>
              <AudioPlayer
                src={item.fields.file.url}
                title={item.fields.title}
                volume={volume}
              />
            </li>
          ))}
        </Container>
      </Main>
    </>
  );
}

interface MainProps {
  navbarCollapse: boolean;
}

export const Main = styled.main<MainProps>`
    display: flex;
    flex-direction: column;
    align-items: center;
    justify-content: center;
    margin-top: ${({ navbarCollapse }) => (navbarCollapse ? "10rem" : "20rem")};
    transition: margin-top 0.2s ease-in-out;
`;

export const Container = styled.ul`
    border-radius: 1rem;
    display: flex;
    flex-wrap: wrap;
    align-items: center;
    justify-content: center;
    padding: 1rem;
    margin: 1rem;
`;
